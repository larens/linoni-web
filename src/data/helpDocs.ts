export const getDocById = (id: string): HelpDoc | undefined => {
  return helpDocs.find(doc => doc.id === id);
};

export const getDocsByCategory = (): Record<string, HelpDoc[]> => {
  return helpDocs.reduce((acc, doc) => {
    if (!acc[doc.category]) {
      acc[doc.category] = [];
    }
    acc[doc.category].push(doc);
    return acc;
  }, {} as Record<string, HelpDoc[]>);
};
